package activityRecords.model;

public class ActivityRecordsInfo {
	private String recordId;
	private String userId;
	private String style;
	private Integer group;
	private Integer pergroup;
	private Integer frequency;
	private Integer path;
	private Integer time;
	private String createDate;
	private String whoCreate;
	private String modifiedDate;
	private String whoModified;
	private String memo;
	public String getRecordId() {
		return recordId;
	}
	public void setRecordId(String recordId) {
		this.recordId = recordId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getStyle() {
		return style;
	}
	public void setStyle(String style) {
		this.style = style;
	}
	public Integer getGroup() {
		return group;
	}
	public void setGroup(Integer group) {
		this.group = group;
	}
	public Integer getPergroup() {
		return pergroup;
	}
	public void setPergroup(Integer pergroup) {
		this.pergroup = pergroup;
	}
	public Integer getFrequency() {
		return frequency;
	}
	public void setFrequency(Integer frequency) {
		this.frequency = frequency;
	}
	public Integer getPath() {
		return path;
	}
	public void setPath(Integer path) {
		this.path = path;
	}
	public Integer getTime() {
		return time;
	}
	public void setTime(Integer time) {
		this.time = time;
	}
	public String getCreateDate() {
		return createDate;
	}
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	public String getWhoCreate() {
		return whoCreate;
	}
	public void setWhoCreate(String whoCreate) {
		this.whoCreate = whoCreate;
	}
	public String getModifiedDate() {
		return modifiedDate;
	}
	public void setModifiedDate(String modifiedDate) {
		this.modifiedDate = modifiedDate;
	}
	public String getWhoModified() {
		return whoModified;
	}
	public void setWhoModified(String whoModified) {
		this.whoModified = whoModified;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	
	
}
